/**
* @brief Function implementing the ADTASK thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_AD_send */
void AD_send(void *argument)
{
  /* USER CODE BEGIN AD_send */

  /* Infinite loop */
  for(;;)
  {
    if (cont > 48)
    {
      CDC_Transmit_FS(data, sizeof(data));
      cont = 0;
    }
    data[cont] = (uint8_t)(adc[0] / 100);
    data[cont + 1] = (uint8_t)(adc[0] - data[cont] * 100);
    cont += 2;
    osDelay(10);
  }
  /* USER CODE END AD_send */
}

/* USER CODE BEGIN Header_SERV */
/**
* @brief Function implementing the MOTORTASK thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_SERV */
void SERV(void *argument)
{
  /* USER CODE BEGIN SERV */
  /* Infinite loop */
  for(;;)
  {
		switch (mode){
			
		case 0:
			
		HAL_GPIO_WritePin(GPIOC,GPIO_PIN_13,0);
		
		break;
		
		case 1:
			
		HAL_GPIO_WritePin(GPIOC,GPIO_PIN_13,1);
		
		HAL_UART_Receive(&huart1,response,1,10);
		
		if (response[0]== 0){
		vcp = ((0*1e-3/90)+0.5e-3)*(3600/20e-3);
		__HAL_TIM_SET_COMPARE(&htim1,TIM_CHANNEL_1,(int)vcp);
		}
		if (response[0]== 1){
		vcp = ((180*1e-3/90)+0.5e-3)*(3600/20e-3);
		__HAL_TIM_SET_COMPARE(&htim1,TIM_CHANNEL_1,(int)vcp);
		}
		break;
			
	}
    osDelay(1);
  }
  /* USER CODE END SERV */
}

